 /**
  * Boolean hanya memiliki 2 nilai benar(true) dan salah(False)
  * Berikut contoh nilai bolean sederhana
  */

package BelajarJava_Basic;

public class Boolean {
    
    public static void main(String[] args){
    

        //Setiap variabel boolean hanya bisa memiliki 2 nilai true dan false
        //==================================================================
        boolean Javamudah = true;
        boolean Javasulit = false;

        System.out.println(Javamudah);
        System.out.println(Javasulit);
        //================================================================ 


        //Contoh menampilkan nilai boolean dari perbandingan 2 nilai
        int i = 5;
        int j = 2;
        System.out.println(i<j);

    }
}
